package com.lexinsmart.model;

public class Singlenumber {
	private Integer id;
	private Integer cid;
	private String carno;
	private Float xzdw;
	private Float carheight;
	private String handplanno;
	private String singleno;
	private Integer scantimes;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getCid() {
		return cid;
	}
	public void setCid(Integer cid) {
		this.cid = cid;
	}
	public String getCarno() {
		return carno;
	}
	public void setCarno(String carno) {
		this.carno = carno;
	}
	public Float getXzdw() {
		return xzdw;
	}
	public void setXzdw(Float xzdw) {
		this.xzdw = xzdw;
	}
	public Float getCarheight() {
		return carheight;
	}
	public void setCarheight(Float carheight) {
		this.carheight = carheight;
	}
	public String getHandplanno() {
		return handplanno;
	}
	public void setHandplanno(String handplanno) {
		this.handplanno = handplanno;
	}
	public String getSingleno() {
		return singleno;
	}
	public void setSingleno(String singleno) {
		this.singleno = singleno;
	}
	public Integer getScantimes() {
		return scantimes;
	}
	public void setScantimes(Integer scantimes) {
		this.scantimes = scantimes;
	}
	
}
