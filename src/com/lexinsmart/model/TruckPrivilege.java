package com.lexinsmart.model;

public class TruckPrivilege {

	private Integer id;
	private Integer egid;
	private Integer tid;
	private Integer tyid;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getEgid() {
		return egid;
	}
	public void setEgid(Integer egid) {
		this.egid = egid;
	}
	public Integer getTid() {
		return tid;
	}
	public void setTid(Integer tid) {
		this.tid = tid;
	}
	public Integer getTyid() {
		return tyid;
	}
	public void setTyid(Integer tyid) {
		this.tyid = tyid;
	}
	
}
